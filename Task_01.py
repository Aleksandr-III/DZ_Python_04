#  Вычислить число π c заданной точностью d
# *Пример:* 
# - при $d = 0.001, π = 3.141.$    $10^{-1} ≤ d ≤10^{-10}$

def pi_(k):
    p = 4
    n = 1
    d = 1
    v = 10**(-k)
    while True:
        d = ((1 / (2 * n + 1))*(-1)**n) * 4
        if abs(d) < v:
            break

        p = p + d
        n += 1

    return round(p, k)

z = int(input('Какую точность нужно числа π?(цифр после точки): '))
print(f'число π = {pi_(z)}')
